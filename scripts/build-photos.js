#!/usr/bin/env node
/**
 * build-photos.js
 *
 * 1. Reads EXIF metadata (date + GPS) from ALL originals FIRST, before any
 *    conversion that might strip tags.
 * 2. Converts HEIC/HEIF → JPEG via sips (macOS built-in).
 * 3. Converts MOV/MP4/video → MP4 (H.264) via ffmpeg for web playback.
 * 4. Reverse-geocodes GPS → readable place name via OpenStreetMap Nominatim.
 * 5. Caches results in assets/photos/metadata.json — skips already-processed files.
 * 6. Injects <figure> cards into index.html between PHOTOS:START / PHOTOS:END.
 *    Images get <img>, videos get <video autoplay muted loop playsinline>.
 *    Both show a hover overlay with location + date.
 *
 * Usage:
 *   node scripts/build-photos.js
 *
 * Requires: exiftool  (brew install exiftool)
 *           ffmpeg    (brew install ffmpeg)
 *           sips      (pre-installed on macOS)
 */

'use strict';

const fs           = require('fs');
const path         = require('path');
const https        = require('https');
const { execSync } = require('child_process');

const ROOT          = path.join(__dirname, '..');
const PHOTOS_DIR    = path.join(ROOT, 'assets', 'photos');
const HTML_FILE     = path.join(ROOT, 'index.html');
const METADATA_FILE = path.join(PHOTOS_DIR, 'metadata.json');

const IMAGE_CONVERT_EXT = new Set(['.heic', '.heif']);
const VIDEO_CONVERT_EXT = new Set(['.mov', '.avi', '.mkv', '.m4v']);
const WEB_IMAGE_EXT     = new Set(['.jpg', '.jpeg', '.png', '.webp', '.avif']);
const WEB_VIDEO_EXT     = new Set(['.mp4']);

const START_MARKER = '<!-- PHOTOS:START — generated by scripts/build-photos.js, do not edit manually -->';
const END_MARKER   = '<!-- PHOTOS:END -->';

// ── Helpers ──────────────────────────────────────────────────────────────────

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

/** Parse "40 deg 15' 14.98\" N" → decimal degrees.
 *  ref can be "North"/"South"/"East"/"West", "N"/"S"/"E"/"W", or absent.
 *  When absent, falls back to the direction letter embedded in the DMS string. */
function dmsToDecimal(dmsStr, ref) {
  // Capture degrees, minutes, seconds, and optional trailing direction (N/S/E/W)
  const m = dmsStr.match(/(\d+)\s+deg\s+(\d+)'\s+([\d.]+)"\s*([NSEWnsew])?/);
  if (!m) return null;
  let dec = parseFloat(m[1]) + parseFloat(m[2]) / 60 + parseFloat(m[3]) / 3600;
  // Prefer explicit ref field; fall back to direction embedded in the DMS string
  const dir = (ref || m[4] || '').charAt(0).toUpperCase();
  if (dir === 'S' || dir === 'W') dec = -dec;
  return dec;
}

/** Format EXIF date "2026:03:15 17:45:33" → "Mar 15, 2026" */
function formatDate(exifDate) {
  if (!exifDate) return null;
  const [datePart] = exifDate.split(' ');
  const [y, m, d]  = datePart.split(':').map(Number);
  return new Date(y, m - 1, d).toLocaleDateString('en-US', {
    month: 'short', day: 'numeric', year: 'numeric'
  });
}

/** Build a readable location string from a Nominatim address object */
function formatLocation(address) {
  if (!address) return null;
  const city   = address.city || address.town || address.village || address.hamlet;
  const suburb = address.neighbourhood || address.suburb || address.quarter;
  const state  = address.state;
  const cc     = address.country_code?.toUpperCase();

  if (cc === 'US') {
    const stateAbbr = {
      'New York': 'NY', 'New Jersey': 'NJ', 'California': 'CA',
      'Texas': 'TX', 'Florida': 'FL', 'Illinois': 'IL',
      'Pennsylvania': 'PA', 'Massachusetts': 'MA', 'Washington': 'WA',
    };
    const abbr = stateAbbr[state] || state;
    if (suburb && city) return `${suburb}, ${city}`;
    if (city && abbr)   return `${city}, ${abbr}`;
    return state || 'United States';
  }

  if (city && cc) return `${city}, ${cc}`;
  return address.country || null;
}

/** HTTP GET → parsed JSON */
function fetchJSON(url, headers = {}) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers }, res => {
      let body = '';
      res.on('data', chunk => body += chunk);
      res.on('end', () => {
        try { resolve(JSON.parse(body)); }
        catch (e) { reject(e); }
      });
    }).on('error', reject);
  });
}

/** Reverse-geocode lat/lon → readable place string */
async function reverseGeocode(lat, lon) {
  const url  = `https://nominatim.openstreetmap.org/reverse?format=json&lat=${lat}&lon=${lon}`;
  const data = await fetchJSON(url, {
    'User-Agent': 'quantumstatic.github.io portfolio builder (personal site)'
  });
  return formatLocation(data?.address);
}

// ── Step 1: Read EXIF from ALL originals BEFORE any conversion ───────────────
// (sips and ffmpeg can strip GPS tags — read metadata while we still have it)

const allFiles     = fs.readdirSync(PHOTOS_DIR).filter(f => f !== 'metadata.json');
const needsReading = allFiles.filter(f => {
  const ext = path.extname(f).toLowerCase();
  return IMAGE_CONVERT_EXT.has(ext) || VIDEO_CONVERT_EXT.has(ext) ||
         WEB_IMAGE_EXT.has(ext)     || WEB_VIDEO_EXT.has(ext);
});

let rawExifMap = {}; // filename → raw exiftool entry

if (needsReading.length > 0) {
  const cache = fs.existsSync(METADATA_FILE)
    ? JSON.parse(fs.readFileSync(METADATA_FILE, 'utf8'))
    : {};

  // Only read EXIF for files not yet in cache (use stem for originals that will be renamed)
  const needsExif = needsReading.filter(f => {
    const stem    = path.basename(f, path.extname(f));
    const jpgKey  = stem + '.jpg';
    const mp4Key  = stem + '.mp4';
    return !cache[f] && !cache[jpgKey] && !cache[mp4Key];
  });

  if (needsExif.length > 0) {
    console.log(`Reading EXIF from ${needsExif.length} original(s) before conversion…`);
    const paths = needsExif.map(f => `"${path.join(PHOTOS_DIR, f)}"`).join(' ');
    try {
      const raw = execSync(
        `exiftool -json -DateTimeOriginal -GPSLatitude -GPSLongitude -GPSLatitudeRef -GPSLongitudeRef ${paths}`,
        { encoding: 'utf8' }
      );
      for (const entry of JSON.parse(raw)) {
        rawExifMap[path.basename(entry.SourceFile)] = entry;
      }
    } catch (err) {
      console.warn('  Warning: exiftool read failed:', err.message);
    }
  }
}

// ── Step 2: Convert HEIC/HEIF → JPEG ────────────────────────────────────────

const toConvertImages = allFiles.filter(f => IMAGE_CONVERT_EXT.has(path.extname(f).toLowerCase()));

if (toConvertImages.length > 0) {
  console.log(`\nConverting ${toConvertImages.length} HEIC/HEIF → JPEG…`);
  for (const file of toConvertImages) {
    const src  = path.join(PHOTOS_DIR, file);
    const dest = path.join(PHOTOS_DIR, path.basename(file, path.extname(file)) + '.jpg');
    try {
      execSync(`sips -s format jpeg "${src}" --out "${dest}"`, { stdio: 'pipe' });
      // Transfer EXIF map entry to the new filename
      const stem = path.basename(file, path.extname(file));
      if (rawExifMap[file]) rawExifMap[stem + '.jpg'] = rawExifMap[file];
      fs.unlinkSync(src);
      console.log(`  ✓ ${file} → ${path.basename(dest)}`);
    } catch (err) {
      console.error(`  ✗ ${file}:`, err.message);
    }
  }
}

// ── Step 3: Convert MOV/video → MP4 ─────────────────────────────────────────

const toConvertVideos = allFiles.filter(f => VIDEO_CONVERT_EXT.has(path.extname(f).toLowerCase()));

if (toConvertVideos.length > 0) {
  console.log(`\nConverting ${toConvertVideos.length} video(s) → MP4…`);
  for (const file of toConvertVideos) {
    const src  = path.join(PHOTOS_DIR, file);
    const dest = path.join(PHOTOS_DIR, path.basename(file, path.extname(file)) + '.mp4');
    try {
      // -vcodec h264 -acodec aac: web-compatible, small file
      // -map_metadata 0: preserve GPS + date tags from the original
      // -movflags +faststart: lets browser start playing before full download
      // -vf "scale=trunc(iw/2)*2:trunc(ih/2)*2": ensures even dimensions (H.264 requirement)
      execSync(
        `ffmpeg -i "${src}" -vcodec h264 -acodec aac -map_metadata 0 -movflags +faststart ` +
        `-vf "scale=trunc(iw/2)*2:trunc(ih/2)*2" -y "${dest}"`,
        { stdio: 'pipe' }
      );
      const stem = path.basename(file, path.extname(file));
      if (rawExifMap[file]) rawExifMap[stem + '.mp4'] = rawExifMap[file];
      fs.unlinkSync(src);
      console.log(`  ✓ ${file} → ${path.basename(dest)}`);
    } catch (err) {
      console.error(`  ✗ ${file}:`, err.message);
    }
  }
}

// ── Step 4: Collect all web-ready media ──────────────────────────────────────

const webFiles = fs.readdirSync(PHOTOS_DIR)
  .filter(f => {
    const ext = path.extname(f).toLowerCase();
    return WEB_IMAGE_EXT.has(ext) || WEB_VIDEO_EXT.has(ext);
  })
  .sort();

if (webFiles.length === 0) {
  console.log('No web-ready media in assets/photos/ — nothing to do.');
  process.exit(0);
}

// ── Step 5: Geocode new files, update cache ───────────────────────────────────

(async () => {

  const cache = fs.existsSync(METADATA_FILE)
    ? JSON.parse(fs.readFileSync(METADATA_FILE, 'utf8'))
    : {};

  const needsCache = webFiles.filter(f => !cache[f]);

  if (needsCache.length > 0) {
    console.log(`\nGeocoding ${needsCache.length} new file(s)…`);

    for (const file of needsCache) {
      const meta  = { date: null, location: null };
      // Use pre-read EXIF map (captured before conversion) or read now if already web-ready
      let entry   = rawExifMap[file];

      if (!entry) {
        // File was already web-ready when script started — read EXIF now
        try {
          const raw = execSync(
            `exiftool -json -DateTimeOriginal -GPSLatitude -GPSLongitude -GPSLatitudeRef -GPSLongitudeRef "${path.join(PHOTOS_DIR, file)}"`,
            { encoding: 'utf8' }
          );
          entry = JSON.parse(raw)[0];
        } catch (_) {}
      }

      if (entry) {
        meta.date = formatDate(entry.DateTimeOriginal);

        if (entry.GPSLatitude && entry.GPSLongitude) {
          const lat = dmsToDecimal(entry.GPSLatitude,  entry.GPSLatitudeRef  || 'N');
          const lon = dmsToDecimal(entry.GPSLongitude, entry.GPSLongitudeRef || 'E');
          if (lat !== null && lon !== null) {
            try {
              process.stdout.write(`  Geocoding ${file}… `);
              meta.location = await reverseGeocode(lat, lon);
              console.log(meta.location || '(no result)');
              await sleep(1100); // Nominatim: max 1 req/s
            } catch (err) {
              console.log(`failed (${err.message})`);
            }
          }
        }
      }

      cache[file] = meta;
    }

    fs.writeFileSync(METADATA_FILE, JSON.stringify(cache, null, 2), 'utf8');
    console.log('  Cache saved → assets/photos/metadata.json');
  }

  // ── Step 6: Generate <figure> cards ─────────────────────────────────────────

  const indent  = '        '; // 8 spaces
  const figures = webFiles.map(f => {
    const ext      = path.extname(f).toLowerCase();
    const isVideo  = WEB_VIDEO_EXT.has(ext);
    const meta     = cache[f] || {};
    const location = meta.location || '';
    const date     = meta.date     || '';

    // Build a meaningful alt string from available metadata
    const altParts = [location, date].filter(Boolean);
    const alt      = altParts.join(', ');

    const media = isVideo
      ? `${indent}  <video autoplay muted loop playsinline>\n` +
        `${indent}    <source src="assets/photos/${f}" type="video/mp4" />\n` +
        `${indent}  </video>`
      : `${indent}  <img src="assets/photos/${f}" alt="${alt}" loading="lazy" />`;

    const overlay = (location || date)
      ? `\n${indent}  <figcaption class="photo-meta">\n` +
        (location ? `${indent}    <span class="photo-location">${location}</span>\n` : '') +
        (date     ? `${indent}    <span class="photo-date">${date}</span>\n`         : '') +
        `${indent}  </figcaption>`
      : '';

    return (
      `${indent}<figure class="photo-item">\n` +
      `${media}${overlay}\n` +
      `${indent}</figure>`
    );
  }).join('\n');

  const block = `${START_MARKER}\n${figures}\n        ${END_MARKER}`;

  // ── Step 7: Inject into index.html ──────────────────────────────────────────

  let html = fs.readFileSync(HTML_FILE, 'utf8');

  const startIdx = html.indexOf(START_MARKER);
  const endIdx   = html.indexOf(END_MARKER);

  if (startIdx === -1 || endIdx === -1) {
    console.error('\n✗ Could not find PHOTOS:START / PHOTOS:END markers in index.html');
    process.exit(1);
  }

  html = html.slice(0, startIdx) + block + html.slice(endIdx + END_MARKER.length);
  fs.writeFileSync(HTML_FILE, html, 'utf8');

  console.log(`\n✓ Injected ${webFiles.length} media file(s) into index.html:`);
  webFiles.forEach(f => {
    const ext  = path.extname(f).toLowerCase();
    const type = WEB_VIDEO_EXT.has(ext) ? 'video' : 'photo';
    const m    = cache[f] || {};
    const info = [m.location, m.date].filter(Boolean).join(' · ');
    console.log(`  • [${type}] ${f}${info ? `  (${info})` : ''}`);
  });

})();
